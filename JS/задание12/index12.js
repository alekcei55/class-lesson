function getDayName(date) {
const days = ["Воскресенье","Понедельник","Вторник","Среда","Четверг","Пятница","Суббота"];
let numDay =date.getDay()     
    return days[numDay]
}



console.log(getDayName(new Date(2024, 0, 15))); // "понедельник"




























































